const STATE_INDEX: Record<ConservationState, number> = {
  A: 0, B: 1, C: 2, D: 3, E: 4, F: 5, G: 6, H: 7,
};

export function getRHFactor(pctVida: number, state: ConservationState): { K: number; FK_RH: number } {
  // Round to nearest even, clamp 2–100
  let rounded = Math.round(pctVida / 2) * 2;
  if (rounded < 2) rounded = 2;
  if (rounded > 100) rounded = 100;

  const row = RH_DATA[rounded];
  const K = row[STATE_INDEX[state]];
  const FK_RH = (100 - K) / 100;
  return { K, FK_RH };
}
